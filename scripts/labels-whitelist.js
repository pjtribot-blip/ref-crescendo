/**
 * Normalise un label candidat. Retourne null si non fiable.
 * - Nettoie les espaces insécables (\xa0, &nbsp;)
 * - Fusionne les espaces multiples
 * - Match exact whitelist, puis aliases, puis préfix-match
 */
function normalizeLabel(raw) {
  if (!raw || typeof raw !== 'string') return null;
  // v3.2 : nettoyer \xa0 et fusionner espaces
  let t = raw.replace(/\u00a0/g, ' ').replace(/\s+/g, ' ').trim();
  if (!t) return null;
  if (/^[:;–—\-«"]/.test(t)) return null;
  if (t.length > 60) return null;

  for (const w of LABELS_WHITELIST) {
    if (t.toLowerCase() === w.toLowerCase()) return w;
  }
  if (LABELS_ALIASES[t]) return LABELS_ALIASES[t];
  for (const [pat, canonical] of LABELS_PREFIX_PATTERNS) {
    if (pat.test(t)) return canonical;
  }
  return null;
}

